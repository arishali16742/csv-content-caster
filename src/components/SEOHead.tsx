import React from 'react';
import { Helmet } from 'react-helmet-async';

interface SEOHeadProps {
  title?: string;
  description?: string;
  keywords?: string;
  image?: string;
  url?: string;
  type?: 'website' | 'article' | 'product';
  structuredData?: object;
  canonical?: string;
}

const SEOHead: React.FC<SEOHeadProps> = ({
  title = "Travellata - Luxury Travel Packages at 20% Less Cost | Book Best Travel Deals",
  description = "Discover handcrafted luxury travel packages 10-20% cheaper than major booking sites. Expert human curation, no bots. Book Bali, Dubai, Europe trips with flights & hotels.",
  keywords = "travel packages, luxury travel, cheap flights, hotel booking, vacation deals, travel agency, Bali packages, Dubai tours, Europe trips, budget travel",
  image = "https://travellata.com/og-image.jpg",
  url = "https://travellata.com",
  type = "website",
  structuredData,
  canonical
}) => {
  return (
    <Helmet>
      {/* Primary Meta Tags */}
      <title>{title}</title>
      <meta name="title" content={title} />
      <meta name="description" content={description} />
      <meta name="keywords" content={keywords} />
      <meta name="robots" content="index, follow" />
      {canonical && <link rel="canonical" href={canonical} />}
      
      {/* Open Graph / Facebook */}
      <meta property="og:type" content={type} />
      <meta property="og:url" content={url} />
      <meta property="og:title" content={title} />
      <meta property="og:description" content={description} />
      <meta property="og:image" content={image} />
      <meta property="og:image:width" content="1200" />
      <meta property="og:image:height" content="630" />
      <meta property="og:site_name" content="Travellata" />
      
      {/* Twitter */}
      <meta property="twitter:card" content="summary_large_image" />
      <meta property="twitter:url" content={url} />
      <meta property="twitter:title" content={title} />
      <meta property="twitter:description" content={description} />
      <meta property="twitter:image" content={image} />
      <meta property="twitter:creator" content="@travellata" />
      
      {/* Structured Data */}
      {structuredData && (
        <script type="application/ld+json">
          {JSON.stringify(structuredData)}
        </script>
      )}
    </Helmet>
  );
};

export default SEOHead;